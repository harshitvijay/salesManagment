    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String user = txtuser.getText();
            String pass = txtpass.getText();
            String status   = txtstat.getSelectedItem().toString();
            pst = (PreparedStatement) conn.prepareStatement("insert into cashier(username,password,status) values(?,?,?)");
            pst.setString(1,user);
            pst.setString(2,pass);
            pst.setString(3,status);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Record inserted in cashier");
            
            txtuser.setText("");
            txtpass.setText("");
            txtstat.setSelectedIndex(-1);
            txtuser.requestFocus();
            tableUpdate();
            
        } catch (SQLException ex) {
            Logger.getLogger(Cashier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

